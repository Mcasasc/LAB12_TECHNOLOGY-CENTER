    private void btnActCursoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActCursoActionPerformed
        try {
            // Obtener los datos ingresados por el usuario para actualizar el empleado
            int idCurso = Integer.parseInt(txtIdCurso.getText());
            String nomCurso = txtNomCurso.getText();
            int numVacantes = Integer.parseInt(txtVacantes.getText());
            int numMatriculados = Integer.parseInt(txtMatriculados.getText());
            String nomProfesor= txtProfesor.getText();
            double precio= Double.parseDouble(txtPrecio.getText());

            // Crear un objeto Empleado con los datos recopilados
            Cursos curso = new Cursos(idCurso, nomCurso, numVacantes, numMatriculados, nomProfesor, precio);

            // Llamar al método ActEmpleado para actualizar los datos del empleado
            TechCenterController control = new TechCenterController(); 
            Cursos cursoActualizado = control.ActCurso(curso); 

            // Verificar si la actualización fue exitosa y mostrar un mensaje
            if (cursoActualizado != null) {
                JOptionPane.showMessageDialog(this, "Datos del curso actualizados correctamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "No se pudo actualizar el curso.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Error en el formato de datos.", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (SQLException ex) {
            Logger.getLogger(ActEmpleadoView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnActCursoActionPerformed


