    private void btnConsultarCursoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarCursoActionPerformed
        try {
            TechCenterController control = new TechCenterController();

            //Seteando datos curso
            txtNomCurso.setText(control.buscarCurso(Integer.parseInt(txtIdCurso.getText())).getNombre());
            txtVacantes.setText(Integer.toString(control.buscarCurso(Integer.parseInt(txtIdCurso.getText())).getNum_vacantes()));
            txtProfesor.setText(control.buscarCurso(Integer.parseInt(txtIdCurso.getText())).getProfesor());
            txtPrecio.setText(Double.toString(control.buscarCurso(Integer.parseInt(txtIdCurso.getText())).getPrecio()));
            txtMatriculados.setText(Integer.toString(control.buscarCurso(Integer.parseInt(txtIdCurso.getText())).getNum_matriculados()));
        } catch (SQLException ex) {
            Logger.getLogger(RegMatriculasView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnConsultarCursoActionPerformed

